const mongoose = require('mongoose')
const loginschema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  cpassword: {
    type: String,
    required: true,
  },
})

const Register = new mongoose.model('Register', loginschema)
module.exports = Register
